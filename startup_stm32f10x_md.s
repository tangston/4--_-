; Reset handler     复位程序
Reset_Handler    PROC
                 EXPORT  Reset_Handler             [WEAK]   ;弱定义，先调用用户定义的，用户没设置，就调用自带的这个
     IMPORT  __main                                         ;IMPORT相当于C的extern
     IMPORT  SystemInit
                 LDR     R0, =SystemInit       ;左边是CM4内核的指令   
                 BLX     R0                   ;BLX加载了后面的之后还要回来  
                 LDR     R0, =__main
                 BX      R0                  ;BX执行完之后不回来
                 ENDP

