; Amount of memory (in bytes) allocated for Stack 栈以字节的形式分配内存，大一C语言里有说
; Tailor this value to your application needs 需要的时候可以裁剪这个值来适应你的程序需求
; <h> Stack Configuration
;   <o> Stack Size (in Bytes) <0x0-0xFFFFFFFF:8>
; </h>
;EQU相当于C语言的#define,具体查证可打开 KeilC的IDE，Help->uVision help->搜索->EQU->Assembler User Guide:EQU->双击后看开头介绍和Examples和Usage
;常用的ARM汇编意思表格，刘火良都总结到零死角教程15章启动文件详解去了，firebbs牛逼
Stack_Size      EQU     0x00000400;自己计算一下就知道是2的10次方1k

                AREA    STACK, NOINIT, READWRITE, ALIGN=3
     ;新 代码/数据 段    栈，   不初始化，可读可写  ，2的3次方对齐
Stack_Mem       SPACE   Stack_Size
;              空间      上面说明的1k
__initial_sp


; <h> Heap Configuration
;   <o>  Heap Size (in Bytes) <0x0-0xFFFFFFFF:8>
; </h>

Heap_Size       EQU     0x00000200      ;memalloc函数就是动态分配的堆空间，而默认分配的是0.5k

                AREA    HEAP, NOINIT, READWRITE, ALIGN=3
__heap_base
Heap_Mem        SPACE   Heap_Size
__heap_limit

